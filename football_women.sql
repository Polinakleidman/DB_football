CREATE TABLE IF NOT EXISTS football_women.player (
    player_id SERIAL PRIMARY KEY NOT NULL,
	player_nm VARCHAR(50) NOT NULL,
	player_amplua VARCHAR(50) NOT NULL,
	player_age INT NOT NULL,
	player_goals_cnt INT NOT NULL
);


CREATE TABLE IF NOT EXISTS football_women.player_history (
    player_id SERIAL REFERENCES football_women.player(player_id) ON DELETE CASCADE NOT NULL,
	player_nm VARCHAR(50) NOT NULL,
	player_amplua VARCHAR(50),
	player_age INT,
	player_goals_cnt INT,
    history_dttm DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.game (
    game_id SERIAL PRIMARY KEY NOT NULL,
	game_place VARCHAR(50) NOT NULL,
	gate_dttm DATE NOT NULL,
	game_score VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.player_game_stats (
    player_id SERIAL REFERENCES football_women.player(player_id) ON DELETE CASCADE NOT NULL,
	game_id SERIAL REFERENCES football_women.game(game_id) ON DELETE CASCADE NOT NULL,
	goals_cnt INT,
	penalty_cards_cnt INT
);

CREATE TABLE IF NOT EXISTS football_women.team (
    team_id SERIAL PRIMARY KEY NOT NULL,
	team_university VARCHAR(100) NOT NULL,
	team_points INT NOT NULL,
	team_place INT NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.game_teams (
    game_id SERIAL REFERENCES football_women.game(game_id) ON DELETE CASCADE NOT NULL,
	team1_id SERIAL REFERENCES football_women.team(team_id) ON DELETE CASCADE NOT NULL,
	team1_points INT NOT NULL,
	team2_id SERIAL REFERENCES football_women.team(team_id) ON DELETE CASCADE NOT NULL,
	team2_points INT NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.team_players (
	team_id SERIAL REFERENCES football_women.team(team_id) ON DELETE CASCADE NOT NULL,
	player_id SERIAL REFERENCES football_women.player(player_id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.league (
    league_id SERIAL PRIMARY KEY NOT NULL,
	league_nm VARCHAR(30) NOT NULL,
	league_cnt INT NOT NULL
);

CREATE TABLE IF NOT EXISTS football_women.league_teams (
    league_id SERIAL REFERENCES football_women.league(league_id) ON DELETE CASCADE NOT NULL,
	team_id SERIAL REFERENCES football_women.team(team_id) ON DELETE CASCADE NOT NULL
);

